a = 80
b = 3

#penjumblahan+
hasil = a+b
print(a,'+',b,'=', hasil)

#perkalian*
hasil = a*b
print(a,'*',b,'+', hasil)

#eksponen(pangkat)**
hasil = a**b
print(a,'**',b,'=', hasil)

#modulus(sisa pembagian)%
hasil = a % b
print(a,'%',b,"=", hasil)

#oprasi floor division(dibulatkan ke bawah) //
hasil = a // b
print(a,'//', b, hasil)

#prioritas oprasi(seperti halnya pada oprasi matematika yg mendahulukan "*" dulu baru "+")
x = 2
y = 4
z = 3
hasil = x + y * z ** y // z + y
print(x, '+', y, '*', z,'**', y, '//', z, '+', y, '=', hasil)


